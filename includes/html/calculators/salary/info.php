    <div class="fd--section-heading">
        <h2>National Insurance Information</h2>
    </div>
    <p>
        Here are some key points about National Insurance in the UK:
    </p>
    <ul class="info--lists">
        <li>
            National Insurance (NI) is a tax that is used to fund state benefits such as the National Health Service (NHS), the State Pension, and other social security benefits.
        </li>
        <li>
            Most workers in the UK have to pay National Insurance contributions if they are aged 16 or over and earn more than a certain amount.
        </li>
        <li>
            Employees pay Class 1 National Insurance contributions, which are deducted from their pay by their employer. For the tax year 2022/23, the Class 1 National Insurance rates are as follows:
            <ul class="uk-margin-small-top">
                <li>
                    12% on earnings between £9,568 and £50,270
                </li>
                <li>
                    2% on earnings above £50,270
                </li>
            </ul>
        </li>
        <li>
            Self-employed workers pay Class 2 and Class 4 National Insurance contributions. Class 2 contributions are a flat rate of £3.05 per week, and are payable if your profits are above a certain threshold. Class 4 contributions are a percentage of your profits, and are payable if your profits are above a different threshold. For the tax year 2022/23, the Class 4 National Insurance rates are as follows:
            <ul class="uk-margin-small-top">
                <li>
                    9% on profits between £9,568 and £50,270
                </li>
                <li>
                    2% on profits above £50,270
                </li>
            </ul>
        </li>
        <li>
            Some workers, such as those who earn less than the National Insurance threshold, may be able to pay voluntary National Insurance contributions to protect their entitlement to certain state benefits.
        </li>
        <li>
            You can check your National Insurance record and make sure you are paying the right amount of National Insurance contributions by setting up a Personal Tax Account on the HM Revenue & Customs (HMRC) website.
        </li>
    </ul> 
