        <h2>Income Tax Information</h2>
    </div>
    <p>
        In the UK, the tax year runs from 6 April to 5 April the following year.
    </p>
    <p>
        The amount of income tax you pay can be reduced by certain tax reliefs and allowances, such as the Personal Savings Allowance and the Marriage Allowance.
    </p>
    <p>
        Self-employed workers in the UK have to pay income tax on their profits, which is the income they earn after deducting their business expenses. The tax rates and bands for self-employed workers are the same as for employees.
    </p>
    <p>
        Income tax rates in the UK are divided into bands. Each band has a different tax rate, so you pay a higher percentage of tax on your income as your earnings increase. For the tax year 2022/23, the tax rates and bands are as follows:
    </p>
    <ul class="info--lists">
        <li>
            0% on income up to £12,570 (personal allowance)
        </li>
        <li>
            20% on income between £12,571 and £50,270
        </li>
        <li>
            40% on income between £50,271 and £125,140
        </li>
        <li>
            45% on income over £125,140
        </li>
    </ul> 
    <p>
        It is illegal for your employer to pay you less than the National Minimum/Living Wage, so check your pay and talk to your manager to make sure you’re getting the pay rate increase you are entitled to.
