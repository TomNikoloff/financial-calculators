        <h2>National Living & Minimum Wages</h2>
    </div>
    <p>
        The UK has a national minimum wage, which is the lowest hourly rate that employers are legally required to pay most workers. The minimum wage varies depending on the age of the worker and whether they are an apprentice. As of April 2023, the minimum wage rates will be as follows:
    </p>
    <ul class="info--lists">
        <li>
            £10.42 per hour for workers aged 23 and over
        </li>
        <li>
            £10.18 per hour for workers aged 21 to 22
        </li>
        <li>
            £7.49 per hour for workers aged 18 to 20
        </li>
        <li>
            £5.28 per hour for workers under 18
        </li>
        <li>
            £5.28 per hour for apprentices (or the apprentice rate if they are aged 19 or over and in the first year of their apprenticeship)
        </li>
    </ul> 
    <p>
        It is illegal for your employer to pay you less than the National Minimum/Living Wage, so check your pay and talk to your manager to make sure you’re getting the pay rate increase you are entitled to.
    </p>
</div>

