        <h2>Basic Credit Card Information</h2>
    </div>    
    <p>
        A credit card is a type of financial product that allows you to borrow money up to a certain limit, known as your credit limit. You can use the card to make purchases or withdraw cash, up to the credit limit, and then repay the amount you've borrowed over time.
    </p>
    <p>
        Here are some key points to keep in mind when it comes to credit cards in the UK:
    </p>
    <ul class="info--lists">
        <li>
            Credit cards allow you to borrow money up to a certain limit and repay it over time.
        </li>
        <li>
            Each month, you'll receive a statement that shows your balance, transactions, and minimum payment due.
        </li>
        <li>
            Interest is charged on any outstanding balance, so it's important to pay off as much as you can each month.
        </li>
        <li>
            Some credit cards charge additional fees, such as an annual fee or foreign transaction fee.
        </li>
        <li>
            There are different types of credit cards available, such as balance transfer cards and rewards cards.
        </li>
        <li>
            When choosing a credit card, consider the interest rate, fees, and any additional benefits or rewards.
        </li>
        <li>
            Your credit score will impact your eligibility for certain cards and the interest rate you're offered.
        </li>
        <li>
            It's important to use credit cards responsibly and only borrow what you can afford to repay to avoid negative impacts on your credit score and financial difficulties.
        </li>
    </ul>
</div>

